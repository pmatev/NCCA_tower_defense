#ifndef GAME_H
#define GAME_H

#include "fwd/game.h"

//-------------------------------------------------------------------//
/// @file game.h
/// @brief a class to manage the game state and delegate key/mouse events
/// to other managers
/// @author Peter Matev
/// @version 1
/// @date 29/11/12
/// Revision History :
/// Initial Version 29/11/12
/// @class Game
//-------------------------------------------------------------------//

class Game
{
public:
    //-------------------------------------------------------------------//
    /// @brief the destructor
    //-------------------------------------------------------------------//
    ~Game();
    //-------------------------------------------------------------------//
    /// @brief constructs the object and returns smart pointer
    //-------------------------------------------------------------------//
    static GamePtr create();
    //-------------------------------------------------------------------//
    /// @brief initiates the game. Calls update() and draw().
    //-------------------------------------------------------------------//
    void run();
    //-------------------------------------------------------------------//
    /// @brief updates the game state
    //-------------------------------------------------------------------//
    void update();
    //-------------------------------------------------------------------//
    /// @brief draws the current game state
    //-------------------------------------------------------------------//
    void draw();

    //-------------------------------------------------------------------//
    /// @brief getter for target frame rate
    //-------------------------------------------------------------------//
    inline const int getFrameRate(){return m_framerate;}

protected:
    //-------------------------------------------------------------------//
    /// @brief hidden ctor
    //-------------------------------------------------------------------//
    Game();

    //-------------------------------------------------------------------//
    /// @brief holds target frame rate.
    //-------------------------------------------------------------------//
    int m_framerate;

};

#endif // GAME_H
